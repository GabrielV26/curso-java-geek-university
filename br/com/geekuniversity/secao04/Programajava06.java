package br.com.geekuniversity.secao04;

public class Programajava06 {
	
	public static void main(String[] args) {
		int number = 5;
	    
		// swi ctrl+espaço
		switch (number) {
		    case 1:
		    	System.out.println("numero: 2");
		    	break;
		    case 2:
		    	System.out.println("numero: 2");
		    	break;
		    case 3:
		    	System.out.println("numero: 3");
		    	break;
		    default:
		    	System.out.println("O numero é: " + number);
			break;
		}
      
	}

}
